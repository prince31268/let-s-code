#include<bits/stdc++.h>
using namespace std;
int main(){
    int a=48;
    cout<<(a>>4)<<endl;     //">>" is right shift operator
}
//NOTE
/*
In one right shift , the given number is divided by 2.
for example:
if the given number is 48,then
in 1st right shift: it becomes 24.
In 2nd right shift: it becomes 12.
In 3rd right shift: it becomes 6.
In 4th right shift: it becomes 3. and so on,....
*/