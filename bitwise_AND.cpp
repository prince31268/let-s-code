#include<bits/stdc++.h>
using namespace std;
int main(){
    int a=4;
    int b=6;
    cout<<"The bitwise AND of a & b is :"<<(a&b)<<endl;
    cout<<"The bitwise OR of a | b is :"<<(a|b)<<endl;
    cout<<"The bitwise NOT of a  is :"<<(~a)<<endl;
    cout<<"The bitwise XOR of a ^ b is :"<<(a^b)<<endl;
}